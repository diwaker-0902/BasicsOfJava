public class usingFunSumOf2Digits {
    public static int addition(int a, int b) {
        int sum = a + b;
        return sum;

    }

    public static void main(String[] args) {

        int a = 2;
        int b = 9;

        int sum = addition(a, b);
        System.out.println("Sum of two digits : " + sum);
    }

}
